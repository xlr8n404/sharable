/**
 * Search for GIFs using Giphy API
 */
export async function searchGifs(query: string, limit: number = 20): Promise<GifResult[]> {
  if (!query.trim()) {
    // Return trending GIFs if no query
    try {
      const res = await fetch(
        `https://api.giphy.com/v1/gifs/trending?api_key=${GIPHY_API_KEY}&limit=${limit}`
      );
      const data = await res.json();
      return data.data || [];
    } catch (error) {
      console.error('Failed to fetch trending GIFs:', error);
      return [];
    }
  }

  try {
    const res = await fetch(
      `https://api.giphy.com/v1/gifs/search?api_key=${GIPHY_API_KEY}&q=${encodeURIComponent(query)}&limit=${limit}`
    );
    const data = await res.json();
    return data.data || [];
  } catch (error) {
    console.error('Failed to search GIFs:', error);
    return [];
  }
}

