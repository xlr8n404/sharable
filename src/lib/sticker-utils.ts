/**
 * Insert sticker into text at cursor position
 */
export function insertStickerAtCursor(
  textarea: HTMLTextAreaElement,
  sticker: string,
  currentContent: string,
  setContent: (content: string) => void
): void {
  const pos = textarea.selectionStart;
  const before = currentContent.substring(0, pos);
  const after = currentContent.substring(pos);
  setContent(before + sticker + after);
  
  setTimeout(() => {
    textarea.focus();
    const newPos = pos + sticker.length;
    textarea.setSelectionRange(newPos, newPos);
  }, 0);
}

/**
 * Insert GIF URL into text at cursor position
 */
export function insertGifAtCursor(
  textarea: HTMLTextAreaElement,
  gifUrl: string,
  gifTitle: string,
  currentContent: string,
  setContent: (content: string) => void
): void {
  const pos = textarea.selectionStart;
  const before = currentContent.substring(0, pos);
  const after = currentContent.substring(pos);
  const gifMarkup = `[GIF: ${gifTitle}](${gifUrl})`;
  setContent(before + gifMarkup + after);
  
  setTimeout(() => {
    textarea.focus();
    const newPos = pos + gifMarkup.length;
    textarea.setSelectionRange(newPos, newPos);
  }, 0);
}
